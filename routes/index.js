var router = require('express').Router();

// ---------------------------------------------------------------------------------------------------------------------
// Homepage
// ---------------------------------------------------------------------------------------------------------------------

router.get('/', function (req, res) {
    if (req.user && req.user.gitlab && !req.user.admin) {
        res.render('pages/logout');
    } else {
        res.redirect(req.urls.generate('/dashboard'));
    }
});

// ---------------------------------------------------------------------------------------------------------------------
// Export
// ---------------------------------------------------------------------------------------------------------------------

module.exports = router;